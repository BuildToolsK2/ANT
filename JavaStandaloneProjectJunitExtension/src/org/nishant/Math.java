package org.nishant;


/**
 * Sample class with simple math operations
 * 
 * @author nishant
 */
public class Math {

	public int add(int a, int b) {
		return a + b;
	}

	public int subtract(int a, int b) {
		return a - b;
	}

	public int multiply(int a, int b) {
		return a * b;
	}

	public int divide(int a, int b) {
		return a / b;
	}
}