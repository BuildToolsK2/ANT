package org.nishant;

/**
 * Sample class with simple math operations
 * 
 * @author nishant
 */
public class Math {

	/**
	 * To add two positive integers
	 * 
	 * @param a
	 * @param b
	 * @return sum of two integers
	 */
	public int add(int a, int b) {
		return a + b;
	}

	/**
	 * To subtract two positive integers
	 * 
	 * @param a
	 * @param b
	 * @return subtract of two integers
	 */
	public int subtract(int a, int b) {
		return a - b;
	}

	/**
	 * To multiply two positive integers
	 * 
	 * @param a
	 * @param b
	 * @return multiplication of two integers
	 */
	public int multiply(int a, int b) {
		return a * b;
	}

	/**
	 * To divide two positive integers
	 * 
	 * @param a
	 * @param b
	 * @return division of two integers
	 */
	public int divide(int a, int b) {
		return a / b;
	}
}